using System;

namespace WhileAndArray
{
    class Program
    {
        static void Main(string[] args)
        {
            int i = 10;

            while (i > 0)
            {
                Console.WriteLine(i);
                i--;
            }
            int j = 0;

            while(j != 11)
            {
                Console.WriteLine(j);
                j++;
            }
        }
    }
}
